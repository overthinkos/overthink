// IsDisposable reports whether the deploy is safe to destroy + rebuild.
func (c Deploy) IsDisposable() bool {
	return (c.Disposable != nil && *c.Disposable) || c.IsEphemeral()
}

// IsGroup reports whether this is a GROUP bundle — no workload cross-ref but
// with sibling members (the cross-deployment subject+driver shape).
func (c Deploy) IsGroup() bool {
	return c.Target == "" && len(c.Members) > 0
}

// IsEphemeral reports whether this deploy is marked ephemeral.
func (c Deploy) IsEphemeral() bool {
	return c.Ephemeral != nil
}

// IsPreemptible reports whether this deploy holds a preemptible exclusive
// resource (graceful-stop side).
func (c Deploy) IsPreemptible() bool {
	return c.Preemptible != nil && len(c.Preemptible.Holds) > 0
}

// PreemptionHolds returns the exclusive-resource token(s) this deploy occupies.
func (c Deploy) PreemptionHolds() []string {
	if c.Preemptible == nil {
		return nil
	}
	return c.Preemptible.Holds
}

// RequiredExclusive returns the exclusive-resource token(s) this deploy claims.
func (c Deploy) RequiredExclusive() []string {
	return c.RequiresExclusive
}

// RequiredShared returns the shared-mode resource token(s) this deploy claims.
func (c Deploy) RequiredShared() []string {
	return c.RequiresShared
}

// LifecycleTag returns the deploy's lifecycle tag.
func (c Deploy) LifecycleTag() string {
	return c.Lifecycle
}

// HasChildren reports whether this node has any nested deployments.
func (n *Deploy) HasChildren() bool {
	return n != nil && len(n.Children) > 0
}

// ---------------------------------------------------------------------------
// Shell (ShellConfig) + DeployShellOverlay — ByShell synthesizes the per-shell
// map from the explicit bash/zsh/fish/sh pointers (the former custom-unmarshal
// map). A non-nil per-shell pointer = that shell was authored.
// ---------------------------------------------------------------------------

func shellMap(bash, zsh, fish, sh *ShellSpec) map[string]*ShellSpec {
	out := map[string]*ShellSpec{}
	if bash != nil {
		out["bash"] = bash
	}
	if zsh != nil {
		out["zsh"] = zsh
	}
	if fish != nil {
		out["fish"] = fish
	}
	if sh != nil {
		out["sh"] = sh
	}
	if len(out) == 0 {
		return nil
	}
	return out
}

// ByShell returns the per-shell overlay map (nil when none authored).
func (s *Shell) ByShell() map[string]*ShellSpec {
	if s == nil {
		return nil
	}
	return shellMap(s.Bash, s.Zsh, s.Fish, s.Sh)
}

// ByShell returns the per-shell overlay map (nil when none authored).
func (o *DeployShellOverlay) ByShell() map[string]*ShellSpec {
	if o == nil {
		return nil
	}
	return shellMap(o.Bash, o.Zsh, o.Fish, o.Sh)
}
