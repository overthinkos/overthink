// OpVerbs (the canonical verb-discriminator set used by Kind()'s error message)
// is CUE-derived from #OpVerb — see vocab_gen.go (regenerated by `task cue:gen`).

// PluginInputStr returns the string value of plugin_input[verb] when this Op is a
// `plugin: <verb>` op carrying that key — "" otherwise. The ONE home (R3) for the host
// check runner (checkrun.go) AND the k8sgen plugin's probe builder (candy/plugin-k8sgen),
// both of which read a migrated `plugin: http`/`addr`/`file`/`command` check subject.
func (c *Op) PluginInputStr(verb string) string {
	if c == nil || c.Plugin != verb || c.PluginInput == nil {
		return ""
	}
	if v, ok := c.PluginInput[verb].(string); ok {
		return v
	}
	return ""
}

// Kind returns the single verb set on the Op, or an error when zero or multiple
// are set (the exactly-one-verb rule).
func (c *Op) Kind() (string, error) {
	set := c.VerbsSet()
	if len(set) == 0 {
		return "", fmt.Errorf("check has no verb set (expected exactly one of: %s)", strings.Join(OpVerbs, ", "))
	}
	if len(set) > 1 {
		return "", fmt.Errorf("check has multiple verbs set (%s); exactly one is required", strings.Join(set, ", "))
	}
	return set[0], nil
}

//nolint:gocyclo // flat exactly-one-verb enumeration over the Op verb fields; extraction would relocate the cases without clarifying
