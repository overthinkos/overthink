// TestParseQuadletDescription_ImageOnly covers the no-instance shape.
func TestParseQuadletDescription_ImageOnly(t *testing.T) {
	dir := t.TempDir()
	unitPath := filepath.Join(dir, "ov-redis.container")
	body := `# ov-redis.container (generated by ov config)
[Unit]
Description=Overthink redis
After=network-online.target

[Container]
Image=ghcr.io/overthinkos/redis:latest
ContainerName=ov-redis
`
	if err := os.WriteFile(unitPath, []byte(body), 0o644); err != nil {
		t.Fatalf("write: %v", err)
	}
	image, instance := parseQuadletDescription(unitPath)
	if image != "redis" {
		t.Errorf("image = %q, want %q", image, "redis")
	}
	if instance != "" {
		t.Errorf("instance = %q, want empty", instance)
	}
}

