// TestParseQuadletDescription_Missing returns ("","") for any
// unparseable input — caller falls back to filename joined name.
func TestParseQuadletDescription_Missing(t *testing.T) {
	dir := t.TempDir()
	cases := []struct {
		name string
		body string
	}{
		{"no Description line", "[Unit]\nAfter=network-online.target\n"},
		{"non-Overthink Description", "[Unit]\nDescription=Some other service\n"},
		{"missing file", "DOES_NOT_EXIST"},
	}
	for _, tc := range cases {
		t.Run(tc.name, func(t *testing.T) {
			path := filepath.Join(dir, tc.name+".container")
			if tc.body != "DOES_NOT_EXIST" {
				if err := os.WriteFile(path, []byte(tc.body), 0o644); err != nil {
					t.Fatalf("write: %v", err)
				}
			} else {
				path = filepath.Join(dir, "no-such.container")
			}
			image, instance := parseQuadletDescription(path)
			if image != "" || instance != "" {
				t.Errorf("expected empty, got image=%q instance=%q", image, instance)
			}
		})
	}
}
